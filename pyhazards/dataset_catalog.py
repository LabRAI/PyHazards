def rendered_dataset_docs(cards: Sequence[DatasetCard]) -> Dict[Path, str]:
    targets: Dict[Path, str] = {
        DATASET_PAGE_PATH: render_dataset_page(cards),
        API_PAGE_PATH: render_dataset_api_page(cards),
    }
    for card in cards:
        targets[card.doc_path] = render_dataset_detail_page(card)
    return targets


def sync_generated_dataset_docs(
    cards: Sequence[DatasetCard],
    check: bool = False,
) -> List[Path]:
    changes: List[Path] = []
    targets = rendered_dataset_docs(cards)
    for path, content in targets.items():
        current = path.read_text(encoding="utf-8") if path.exists() else None
        if current != content:
            changes.append(path)
            if not check:
                path.parent.mkdir(parents=True, exist_ok=True)
                path.write_text(content, encoding="utf-8")

    managed_paths = set(targets.keys())
    if DATASET_DOCS_DIR.exists():
        for path in DATASET_DOCS_DIR.glob("*.rst"):
            if path in managed_paths or not path.exists():
                continue
            text = path.read_text(encoding="utf-8")
            if GENERATED_MARKER not in text:
                continue
            changes.append(path)
            if not check:
                path.unlink()

    return changes


def dataset_catalog_alignment_issues(
    cards: Optional[Sequence[DatasetCard]] = None,
) -> List[str]:
    if cards is None:
        cards = load_dataset_cards()

    from .benchmark_catalog import load_benchmark_cards
    from .datasets import available_datasets
    from .model_catalog import card_by_registry_name, load_model_cards

    issues: List[str] = []
    registered = set(available_datasets())
    benchmark_map = {card.slug: card for card in load_benchmark_cards()}
    model_map = card_by_registry_name(load_model_cards())

    for card in cards:
        if card.inspection is not None and card.inspection.module is not None:
            module_path = REPO_ROOT / (card.inspection.module.replace(".", "/") + ".py")
            if not module_path.exists():
                issues.append(
                    f"Dataset card '{card.slug}' points to missing inspection module "
                    f"'{card.inspection.module}'."
                )
        if card.registry and card.registry.name not in registered:
            issues.append(
                f"Dataset card '{card.slug}' points to unknown dataset registry entry "
                f"'{card.registry.name}'."
            )
        for slug in card.related_benchmarks:
            if slug not in benchmark_map:
                issues.append(
                    f"Dataset card '{card.slug}' links unknown benchmark '{slug}'."
                )
        for model_name in card.related_models:
            if model_name not in model_map:
                issues.append(
                    f"Dataset card '{card.slug}' links unknown model '{model_name}'."
                )
    return issues


