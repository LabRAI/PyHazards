class DatasetLink(BaseModel):
    label: str
    url: str


class DatasetReference(BaseModel):
    citation: str
    url: str
    repo_url: Optional[str] = None


class InspectionSpec(BaseModel):
    command: str
    module: Optional[str] = None
    notes: List[str] = Field(default_factory=list)

    @model_validator(mode="after")
    def derive_module_from_command(self) -> "InspectionSpec":
        if self.module:
            return self
        tokens = shlex_split(self.command)
        if len(tokens) >= 3 and tokens[1] == "-m":
            self.module = tokens[2]
        return self


class RegistrySpec(BaseModel):
    name: str = Field(validation_alias=AliasChoices("name", "dataset_name"))
    example: str
    notes: List[str] = Field(default_factory=list)


class DatasetCard(BaseModel):
    slug: str
    display_name: str
    hazard_family: str
    source_role: str
    summary: str
    provider: str
    geometry: str
    coverage: str
    update_cadence: str
    period_of_record: str
    spatial_resolution: Optional[str] = None
    temporal_resolution: Optional[str] = None
    formats: Optional[str] = None
    overview: List[str] = Field(default_factory=list)
    data_characteristics: List[str] = Field(default_factory=list)
    typical_use_cases: List[str] = Field(default_factory=list)
    access_links: List[DatasetLink] = Field(default_factory=list)
    inspection: Optional[InspectionSpec] = None
    references: List[DatasetReference] = Field(
        default_factory=list,
        validation_alias=AliasChoices("references", "primary_references"),
    )
    pyhazards_usage: List[str] = Field(default_factory=list)
    registry: Optional[RegistrySpec] = None
    related_models: List[str] = Field(default_factory=list)
    related_benchmarks: List[str] = Field(default_factory=list)
    notes: List[str] = Field(default_factory=list)

    @model_validator(mode="after")
    def validate_card(self) -> "DatasetCard":
        if not self.references:
            raise ValueError("dataset cards require at least one reference")
        if self.hazard_family not in HAZARD_DISPLAY_ORDER:
            raise ValueError(
                f"dataset card '{self.slug}' uses unsupported hazard_family={self.hazard_family!r}"
            )
        return self

    @property
    def doc_path(self) -> Path:
        return DATASET_DOCS_DIR / f"{self.slug}.rst"

    @property
    def doc_target(self) -> str:
        return f"datasets/{self.slug}"

    @property
    def primary_surface(self) -> str:
        if self.inspection is not None:
            return f"Inspection: ``{self.inspection.command}``"
        if self.registry is not None:
            return f"Registry: ``load_dataset('{self.registry.name}', ...)``"
        return f"Source: {_single_line(self.references[0].citation)}"


def load_dataset_cards(cards_dir: Path = DATASET_CARDS_DIR) -> List[DatasetCard]:
    cards: List[DatasetCard] = []
    seen_slugs: Set[str] = set()
    for path in sorted(cards_dir.glob("*.y*ml")):
        raw = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
        card = DatasetCard.model_validate(raw)
        if path.stem != card.slug:
            raise ValueError(
                f"Dataset card filename must match slug: {path.name} vs {card.slug}"
            )
        if card.slug in seen_slugs:
            raise ValueError(f"Duplicate dataset card slug detected: {card.slug}")
        seen_slugs.add(card.slug)
        cards.append(card)
    return cards


def _single_line(text: str) -> str:
    return " ".join(text.split())


def _indent_block(text: str, prefix: str = "   ") -> str:
    lines = text.rstrip().splitlines()
    return "\n".join(prefix + line if line else prefix.rstrip() for line in lines)


def _indent_lines(lines: Sequence[str], prefix: str = "   ") -> List[str]:
    return [prefix + line if line else "" for line in lines]


def _badge(role: str, text: str) -> str:
    return f":bdg-{role}:`{text}`"


def _ordered_unique(items: Iterable[str]) -> List[str]:
    seen: Set[str] = set()
    ordered: List[str] = []
    for item in items:
        if item in seen:
            continue
        seen.add(item)
        ordered.append(item)
    return ordered


def _order_key(hazard: str) -> tuple[int, str]:
    if hazard in HAZARD_DISPLAY_ORDER:
        return (HAZARD_DISPLAY_ORDER.index(hazard), hazard.lower())
    return (len(HAZARD_DISPLAY_ORDER), hazard.lower())


def group_cards_by_hazard(cards: Sequence[DatasetCard]) -> Dict[str, List[DatasetCard]]:
    grouped: Dict[str, List[DatasetCard]] = defaultdict(list)
    for card in cards:
        grouped[card.hazard_family].append(card)
    return {
        hazard: sorted(hazard_cards, key=lambda item: item.display_name.lower())
        for hazard, hazard_cards in sorted(
            grouped.items(),
            key=lambda item: _order_key(item[0]),
        )
    }


def _cards_in_display_order(cards: Sequence[DatasetCard]) -> List[DatasetCard]:
    grouped = group_cards_by_hazard(cards)
    ordered: List[DatasetCard] = []
    for hazard in HAZARD_DISPLAY_ORDER:
        ordered.extend(grouped.get(hazard, []))
    return ordered


def _dataset_doc_link(card: DatasetCard, absolute: bool = False) -> str:
    target = f"/{card.doc_target}" if absolute else card.doc_target
    return f":doc:`{card.display_name} <{target}>`"


def _benchmark_link(slug: str, display_name: str, absolute: bool = False) -> str:
    target = f"/benchmarks/{slug}" if absolute else f"benchmarks/{slug}"
    return f":doc:`{display_name} <{target}>`"


def _model_link(display_name: str, doc_slug: str, absolute: bool = False) -> str:
    target = f"/modules/{doc_slug}" if absolute else f"modules/{doc_slug}"
    return f":doc:`{display_name} <{target}>`"


def _dataset_reference_sentence(reference: DatasetReference) -> str:
    sentence = f"`{reference.citation} <{reference.url}>`_"
    if reference.repo_url:
        sentence += f" (`repo <{reference.repo_url}>`__)"
    return sentence + "."


def _primary_reference_link(card: DatasetCard) -> str:
    reference = card.references[0]
    return f"`{reference.citation} <{reference.url}>`_"


def _count_phrase(count: int, noun: str) -> str:
    suffix = "" if count == 1 else "s"
    return f"{count} {noun}{suffix}"


def _stat_card(title: str, value: str, note: str) -> List[str]:
    return [
        f".. grid-item-card:: {title}",
        "   :class-card: catalog-stat-card",
        "",
        "   .. container:: catalog-stat-value",
        "",
        f"      {value}",
        "",
        "   .. container:: catalog-stat-note",
        "",
        f"      {note}",
        "",
    ]


def _reference_links(card: DatasetCard) -> str:
    return f"**Primary Source:** {_primary_reference_link(card)}"


def _inspection_literal(card: DatasetCard) -> str:
    if card.inspection is None:
        return ""
    return f"``{card.inspection.command}``"


def _registry_literal(card: DatasetCard) -> str:
    if card.registry is None:
        return ""
    return f"``load_dataset('{card.registry.name}', ...)``"


def _related_benchmarks(card: DatasetCard, absolute: bool = False) -> List[str]:
    if not card.related_benchmarks:
        return []
    from .benchmark_catalog import load_benchmark_cards

    benchmark_map = {item.slug: item for item in load_benchmark_cards()}
    links: List[str] = []
    for slug in _ordered_unique(card.related_benchmarks):
        benchmark = benchmark_map[slug]
        links.append(_benchmark_link(slug, benchmark.display_name, absolute=absolute))
    return links


def _related_models(card: DatasetCard, absolute: bool = False) -> List[str]:
    if not card.related_models:
        return []
    from .model_catalog import card_by_registry_name, load_model_cards

    model_map = card_by_registry_name(load_model_cards())
    links: List[str] = []
    for model_name in _ordered_unique(card.related_models):
        model_card = model_map[model_name]
        links.append(
            _model_link(
                model_card.display_name,
                model_card.module_doc_name,
                absolute=absolute,
            )
        )
    return links


def _dataset_usage_line(card: DatasetCard) -> List[str]:
    if card.inspection is not None:
        return [
            "   .. container:: catalog-meta-row",
            "",
            f"      **Inspection:** {_inspection_literal(card)}",
            "",
        ]
    if card.registry is not None:
        return [
            "   .. container:: catalog-meta-row",
            "",
            f"      **Registry:** {_registry_literal(card)}",
            "",
        ]
    return []


def _render_dataset_card(card: DatasetCard) -> List[str]:
    role_badge = _badge(
        SOURCE_ROLE_BADGE_ROLES.get(card.source_role, "secondary"),
        card.source_role,
    )
    geometry_badge = _badge(
        GEOMETRY_BADGE_ROLES.get(card.geometry, "info"),
        card.geometry,
    )
    related_benchmarks = _related_benchmarks(card)
    lines = [
        f".. grid-item-card:: {card.display_name}",
        "   :class-card: catalog-entry-card",
        "",
        "   .. container:: catalog-entry-summary",
        "",
        f"      {_single_line(card.summary).rstrip('.')}.",
        "",
        "   .. container:: catalog-chip-row",
        "",
        f"      {role_badge} {geometry_badge}",
        "",
        "   .. container:: catalog-meta-row",
        "",
        f"      **Coverage:** {card.coverage}",
        "",
        "   .. container:: catalog-meta-row",
        "",
        f"      **Update Cadence:** {card.update_cadence}",
        "",
    ]
    lines.extend(_dataset_usage_line(card))
    if related_benchmarks:
        lines.extend(
            [
                "   .. container:: catalog-meta-row",
                "",
                f"      **Related Benchmarks:** {', '.join(related_benchmarks)}",
                "",
            ]
        )
    lines.extend(
        [
            "   .. container:: catalog-link-row",
            "",
            f"      **Details:** {_dataset_doc_link(card)}",
            "",
            "   .. container:: catalog-link-row",
            "",
            f"      {_reference_links(card)}",
            "",
        ]
    )
    return lines


def _render_dataset_grid(cards: Sequence[DatasetCard]) -> List[str]:
    lines: List[str] = [
        ".. grid:: 1 1 2 2",
        "   :gutter: 2",
        "   :class-container: catalog-grid",
        "",
    ]
    for card in cards:
        lines.extend(_indent_lines(_render_dataset_card(card)))
    return lines


def _render_recommended_grid(cards: Sequence[DatasetCard]) -> List[str]:
    by_slug = {card.slug: card for card in cards}
    lines: List[str] = [
        ".. grid:: 1 1 2 4",
        "   :gutter: 2",
        "   :class-container: catalog-recommend-grid",
        "",
    ]
    for hazard in HAZARD_DISPLAY_ORDER:
        slug = STARTER_DATASETS.get(hazard)
        card = by_slug.get(slug) if slug else None
        if card is None:
            continue
        usage_line = card.primary_surface
        lines.extend(
            _indent_lines(
                [
                    f".. grid-item-card:: {hazard}",
                    "   :class-card: catalog-detail-card",
                    "",
                    f"   **Start with:** {_dataset_doc_link(card)}",
                    "",
                    f"   {_single_line(card.summary).rstrip('.')}.",
                    "",
                    f"   **Primary Surface:** {usage_line}",
                    "",
                ]
            )
        )
    return lines


