def _fact_rows(card: DatasetCard) -> List[tuple[str, str]]:
    rows = [
        ("Provider", card.provider),
        ("Hazard Family", card.hazard_family),
        ("Source Role", card.source_role),
        ("Coverage", card.coverage),
        ("Geometry", card.geometry),
    ]
    if card.spatial_resolution:
        rows.append(("Spatial Resolution", card.spatial_resolution))
    if card.temporal_resolution:
        rows.append(("Temporal Resolution", card.temporal_resolution))
    rows.extend(
        [
            ("Update Cadence", card.update_cadence),
            ("Period of Record", card.period_of_record),
        ]
    )
    if card.formats:
        rows.append(("Formats", card.formats))
    if card.inspection is not None:
        rows.append(("Inspection CLI", f"``{card.inspection.command}``"))
    if card.registry is not None:
        rows.append(("Registry Entry", f"``{card.registry.name}``"))
    return rows


