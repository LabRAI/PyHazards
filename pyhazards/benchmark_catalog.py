from __future__ import annotations

from collections import defaultdict
from pathlib import Path
from typing import Dict, Iterable, List, Literal, Optional, Sequence, Set

import yaml
from pydantic import BaseModel, Field, model_validator

from .benchmarks import available_benchmarks, build_benchmark
from .configs import load_experiment_config
from .model_catalog import card_by_registry_name, load_model_cards
from .tasks import has_hazard_task


REPO_ROOT = Path(__file__).resolve().parent.parent
BENCHMARK_CARDS_DIR = Path(__file__).resolve().parent / "benchmark_cards"
DOCS_SOURCE_DIR = REPO_ROOT / "docs" / "source"
BENCHMARK_DOCS_DIR = DOCS_SOURCE_DIR / "benchmarks"
BENCHMARK_PAGE_PATH = DOCS_SOURCE_DIR / "pyhazards_benchmarks.rst"

GENERATED_MARKER = (
    ".. This file is generated by scripts/render_benchmark_docs.py. Do not edit by hand."
)

HAZARD_DISPLAY_ORDER = [
    "Wildfire",
    "Earthquake",
    "Flood",
    "Hurricane",
    "Tropical Cyclone / Hurricane",
    "Tropical Cyclone",
]

SUPPORT_STATUS_LABELS = {
    "synthetic-backed": "Synthetic-backed",
    "real-backed": "Real-backed",
    "experimental": "Experimental",
}


class BenchmarkSource(BaseModel):
    title: str
    url: str
    repo_url: Optional[str] = None


class BenchmarkCard(BaseModel):
    slug: str
    display_name: str
    kind: Literal["family", "ecosystem"]
    hazard_family: str
    benchmark_key: str
    support_status: Literal["synthetic-backed", "real-backed", "experimental"]
    summary: str
    description: List[str]
    tasks: List[str]
    metrics: List[str]
    smoke_configs: List[str]
    linked_models: List[str] = Field(default_factory=list)
    notes: List[str] = Field(default_factory=list)
    source: Optional[BenchmarkSource] = None

    @model_validator(mode="after")
    def validate_card(self) -> "BenchmarkCard":
        if self.kind == "ecosystem" and self.source is None:
            raise ValueError("ecosystem benchmark cards require a source block")
        return self

    @property
    def doc_path(self) -> Path:
        return BENCHMARK_DOCS_DIR / f"{self.slug}.rst"

    @property
    def doc_target(self) -> str:
        return f"benchmarks/{self.slug}"


def load_benchmark_cards(cards_dir: Path = BENCHMARK_CARDS_DIR) -> List[BenchmarkCard]:
    cards: List[BenchmarkCard] = []
    seen_slugs: Set[str] = set()
    for path in sorted(cards_dir.glob("*.y*ml")):
        raw = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
        card = BenchmarkCard.model_validate(raw)
        if path.stem != card.slug:
            raise ValueError(
                "Benchmark card filename must match slug: "
                f"{path.name} vs {card.slug}"
            )
        if card.slug in seen_slugs:
            raise ValueError(f"Duplicate benchmark card slug detected: {card.slug}")
        seen_slugs.add(card.slug)
        cards.append(card)
    return cards


def _ordered_unique(items: Iterable[str]) -> List[str]:
    seen = set()
    ordered: List[str] = []
    for item in items:
        if item not in seen:
            ordered.append(item)
            seen.add(item)
    return ordered


def _single_line(text: str) -> str:
    return " ".join(text.split())


def _benchmark_metrics(benchmark_key: str) -> Dict[str, Sequence[str]]:
    benchmark = build_benchmark(benchmark_key)
    metric_names_by_task = getattr(benchmark, "metric_names_by_task", {})
    return {task: list(metrics) for task, metrics in metric_names_by_task.items()}


def _order_key(hazard: str) -> tuple[int, str]:
    if hazard in HAZARD_DISPLAY_ORDER:
        return (HAZARD_DISPLAY_ORDER.index(hazard), hazard.lower())
    return (len(HAZARD_DISPLAY_ORDER), hazard.lower())


def _group_cards(cards: Sequence[BenchmarkCard], kind: str) -> Dict[str, List[BenchmarkCard]]:
    grouped: Dict[str, List[BenchmarkCard]] = defaultdict(list)
    for card in cards:
        if card.kind == kind:
            grouped[card.hazard_family].append(card)
    return {
        hazard: sorted(hazard_cards, key=lambda item: item.display_name.lower())
        for hazard, hazard_cards in sorted(grouped.items(), key=lambda item: _order_key(item[0]))
    }


def _benchmark_doc_link(card: BenchmarkCard, absolute: bool = False) -> str:
    target = f"/{card.doc_target}" if absolute else card.doc_target
    return f":doc:`{card.display_name} <{target}>`"


def _linked_models(card: BenchmarkCard, absolute: bool = False) -> str:
    if not card.linked_models:
        return "None."
    model_map = card_by_registry_name(load_model_cards())
    links: List[str] = []
    for name in card.linked_models:
        model_card = model_map[name]
        target = (
            f"/modules/{model_card.module_doc_name}"
            if absolute
            else f"modules/{model_card.module_doc_name}"
        )
        links.append(f":doc:`{model_card.display_name} <{target}>`")
    return ", ".join(links) + "."


def _linked_configs(card: BenchmarkCard) -> str:
    if not card.smoke_configs:
        return "None."
    return ", ".join(f"``{Path(path).name}``" for path in card.smoke_configs) + "."


def _source_sentence(card: BenchmarkCard) -> str:
    if card.source is None:
        return ""
    sentence = f"`{card.source.title} <{card.source.url}>`_"
    if card.source.repo_url:
        sentence += f" (`repo <{card.source.repo_url}>`__)"
    return sentence + "."


def _status_label(card: BenchmarkCard) -> str:
    return SUPPORT_STATUS_LABELS[card.support_status]


def _family_lookup(cards: Sequence[BenchmarkCard]) -> Dict[str, BenchmarkCard]:
    return {
        card.benchmark_key: card
        for card in cards
        if card.kind == "family"
    }


def _ecosystem_links(
    cards: Sequence[BenchmarkCard],
    benchmark_key: str,
    absolute: bool = False,
) -> str:
    ecosystems = [
        _benchmark_doc_link(card, absolute=absolute)
        for card in cards
        if card.kind == "ecosystem" and card.benchmark_key == benchmark_key
    ]
    if not ecosystems:
        return "None."
    return ", ".join(ecosystems) + "."


def _row_summary(card: BenchmarkCard, family_lookup: Dict[str, BenchmarkCard]) -> str:
    task_text = ", ".join(f"``{task}``" for task in card.tasks)
    metric_text = ", ".join(f"``{metric}``" for metric in card.metrics)
    support_text = _status_label(card)
    summary_text = _single_line(card.summary).rstrip(".")
    if card.kind == "family":
        return (
            f"{summary_text}."
            f" Covers {task_text}. Key metrics: {metric_text}. "
            f"Support: {support_text}. Smoke configs: {_linked_configs(card)}"
        )

    family = family_lookup[card.benchmark_key]
    source = _source_sentence(card)
    linked_models = _linked_models(card)
    return (
        f"{summary_text}."
        f" Runs through {_benchmark_doc_link(family)} with {task_text}. "
        f"Key metrics: {metric_text}. Support: {support_text}. "
        f"Smoke configs: {_linked_configs(card)} Linked models: {linked_models} "
        f"{source}".strip()
    )


def benchmark_catalog_alignment_issues(cards: Optional[Sequence[BenchmarkCard]] = None) -> List[str]:
    if cards is None:
        cards = load_benchmark_cards()

    issues: List[str] = []
    registered = set(available_benchmarks())
    model_map = card_by_registry_name(load_model_cards())
    family_lookup = _family_lookup(cards)

    for card in cards:
        if card.benchmark_key not in registered:
            issues.append(
                f"Benchmark card '{card.slug}' points to unregistered benchmark '{card.benchmark_key}'."
            )
            continue

        metric_names_by_task = _benchmark_metrics(card.benchmark_key)
        benchmark_tasks = list(metric_names_by_task.keys())
        benchmark_metrics = _ordered_unique(
            metric for task in benchmark_tasks for metric in metric_names_by_task.get(task, [])
        )

        if card.kind == "family" and set(card.tasks) != set(benchmark_tasks):
            issues.append(
                f"Family benchmark card '{card.slug}' tasks do not match benchmark '{card.benchmark_key}'."
            )

        for task in card.tasks:
            if not has_hazard_task(task):
                issues.append(f"Benchmark card '{card.slug}' declares unknown hazard task '{task}'.")
            elif task not in metric_names_by_task:
                issues.append(
                    f"Benchmark card '{card.slug}' uses hazard task '{task}' not declared by '{card.benchmark_key}'."
                )

        for metric in card.metrics:
            if metric not in benchmark_metrics:
                issues.append(
                    f"Benchmark card '{card.slug}' declares metric '{metric}' not exposed by '{card.benchmark_key}'."
                )

        if card.kind == "ecosystem" and card.benchmark_key not in family_lookup:
            issues.append(
                f"Ecosystem benchmark card '{card.slug}' points to benchmark '{card.benchmark_key}' without a family card."
            )

        for config_path in card.smoke_configs:
            path = REPO_ROOT / config_path
            if not path.exists():
                issues.append(f"Smoke config does not exist: {config_path}")
                continue
            config = load_experiment_config(path)
            if config.benchmark.name != card.benchmark_key:
                issues.append(
                    f"Smoke config '{config_path}' targets benchmark '{config.benchmark.name}', "
                    f"expected '{card.benchmark_key}'."
                )
            if config.benchmark.hazard_task not in card.tasks:
                issues.append(
                    f"Smoke config '{config_path}' uses hazard task '{config.benchmark.hazard_task}' "
                    f"outside benchmark card '{card.slug}'."
                )

        for model_name in card.linked_models:
            if model_name not in model_map:
                issues.append(
                    f"Benchmark card '{card.slug}' links unknown model '{model_name}'."
                )

    return issues


def render_benchmark_page(cards: Optional[Sequence[BenchmarkCard]] = None) -> str:
    if cards is None:
        cards = load_benchmark_cards()
    family_lookup = _family_lookup(cards)
    families = _group_cards(cards, "family")
    ecosystems = _group_cards(cards, "ecosystem")

    lines: List[str] = [
        GENERATED_MARKER,
        "",
        "Benchmarks",
        "===================",
        "",
        "Overview",
        "--------",
        "",
        "Use this page to browse the benchmark families implemented in PyHazards",
        "and the Appendix-A benchmark ecosystems they currently align to. Each",
        "linked benchmark entry leads to a detail page with support status, smoke",
        "configs, metrics, and linked models.",
        "",
        "Benchmark Families",
        "------------------",
        "",
        "These are the shared evaluator families exposed through the benchmark",
        "runner. Each row links to a detail page describing the family contract,",
        "tasks, metrics, smoke configs, and mapped external benchmark ecosystems.",
        "",
        ".. list-table::",
        "   :widths: 18 26 56",
        "   :header-rows: 1",
        "   :class: dataset-list",
        "",
        "   * - Hazard Family",
        "     - Benchmark",
        "     - Description",
    ]

    for hazard, hazard_cards in families.items():
        for card in hazard_cards:
            lines.extend(
                [
                    f"   * - {hazard}",
                    f"     - {_benchmark_doc_link(card)}",
                    f"     - {_row_summary(card, family_lookup)}",
                ]
            )

    lines.extend(
        [
            "",
            "Benchmark Ecosystems",
            "--------------------",
            "",
            "These rows cover the Appendix-A benchmark or evaluation ecosystems that",
            "shape the current PyHazards benchmark workflow. They stay separate from",
            "model baselines and link back to the shared benchmark families.",
            "",
            ".. list-table::",
            "   :widths: 18 26 56",
            "   :header-rows: 1",
            "   :class: dataset-list",
            "",
            "   * - Hazard Family",
            "     - Benchmark Ecosystem",
            "     - Description",
        ]
    )

    for hazard, hazard_cards in ecosystems.items():
        for card in hazard_cards:
            lines.extend(
                [
                    f"   * - {hazard}",
                    f"     - {_benchmark_doc_link(card)}",
                    f"     - {_row_summary(card, family_lookup)}",
                ]
            )

    lines.extend(
        [
            "",
            "Typical Usage",
            "-------------",
            "",
            ".. code-block:: python",
            "",
            "    from pyhazards.configs import load_experiment_config",
            "    from pyhazards.engine import BenchmarkRunner",
            "",
            '    config = load_experiment_config("pyhazards/configs/earthquake/phasenet_smoke.yaml")',
            "    summary = BenchmarkRunner().run(config)",
            "    print(summary.metrics)",
            "",
            "Command-Line Entry Point",
            "------------------------",
            "",
            "Use ``python scripts/run_benchmark.py --help`` to inspect the shared",
            "benchmark runner CLI and list available hazard tasks or registered",
            "benchmark families.",
            "",
            "Next step: pair this page with :doc:`pyhazards_configs` when you want",
            "to author experiment YAML files and :doc:`pyhazards_reports` when you",
            "want to export comparable benchmark summaries.",
            "",
            ".. toctree::",
            "   :maxdepth: 1",
            "   :hidden:",
            "",
        ]
    )

    for card in cards:
        lines.append(f"   benchmarks/{card.slug}")

    lines.append("")
    return "\n".join(lines)


def render_benchmark_detail_page(
    card: BenchmarkCard,
    cards: Sequence[BenchmarkCard],
) -> str:
    family_lookup = _family_lookup(cards)
    benchmark = build_benchmark(card.benchmark_key)

    lines: List[str] = [
        GENERATED_MARKER,
        "",
        card.display_name,
        "=" * len(card.display_name),
        "",
        "Overview",
        "--------",
        "",
    ]
    for paragraph in card.description:
        lines.append(_single_line(paragraph))
        lines.append("")

    lines.extend(
        [
            "Benchmark Mapping",
            "-----------------",
            "",
            f"Kind: ``{card.kind}``",
            "",
            f"Hazard family: ``{card.hazard_family}``",
            "",
            f"Shared benchmark key: ``{card.benchmark_key}``",
            "",
            f"Registered class: ``{benchmark.__class__.__name__}``",
            "",
            f"Support status: ``{_status_label(card)}``",
            "",
        ]
    )

    if card.kind == "ecosystem":
        family = family_lookup[card.benchmark_key]
        lines.extend(
            [
                "Mapped benchmark family",
                "~~~~~~~~~~~~~~~~~~~~~~~",
                "",
                _benchmark_doc_link(family, absolute=True),
                "",
            ]
        )
        source = _source_sentence(card)
        if source:
            lines.extend(
                [
                    "Primary Source",
                    "~~~~~~~~~~~~~~",
                    "",
                    source,
                    "",
                ]
            )
    else:
        lines.extend(
            [
                "Mapped benchmark ecosystems",
                "~~~~~~~~~~~~~~~~~~~~~~~~~~~",
                "",
                _ecosystem_links(cards, card.benchmark_key, absolute=True),
                "",
            ]
        )

    lines.extend(
        [
            "Supported Tasks",
            "---------------",
            "",
        ]
    )
    for task in card.tasks:
        lines.append(f"- ``{task}``")
    lines.append("")

    lines.extend(
        [
            "Key Metrics",
            "-----------",
            "",
        ]
    )
    for metric in card.metrics:
        lines.append(f"- ``{metric}``")
    lines.append("")

    lines.extend(
        [
            "Smoke Configs",
            "-------------",
            "",
        ]
    )
    for config_path in card.smoke_configs:
        lines.append(f"- ``{Path(config_path).name}``")
    lines.append("")

    lines.extend(
        [
            "Linked Models",
            "-------------",
            "",
            _linked_models(card, absolute=True),
            "",
        ]
    )

    if card.notes:
        lines.extend(["Notes", "-----", ""])
        for note in card.notes:
            lines.append(f"- {_single_line(note)}")
        lines.append("")

    return "\n".join(lines)


def rendered_benchmark_docs(cards: Optional[Sequence[BenchmarkCard]] = None) -> Dict[Path, str]:
    if cards is None:
        cards = load_benchmark_cards()
    targets: Dict[Path, str] = {
        BENCHMARK_PAGE_PATH: render_benchmark_page(cards),
    }
    for card in cards:
        targets[card.doc_path] = render_benchmark_detail_page(card, cards)
    return targets


def sync_generated_benchmark_docs(check: bool = False) -> List[Path]:
    cards = load_benchmark_cards()
    targets = rendered_benchmark_docs(cards)
    changes: List[Path] = []

    for path, content in targets.items():
        current = path.read_text(encoding="utf-8") if path.exists() else None
        if current != content:
            changes.append(path)
            if not check:
                path.parent.mkdir(parents=True, exist_ok=True)
                path.write_text(content, encoding="utf-8")

    managed_paths = set(targets.keys())
    if BENCHMARK_DOCS_DIR.exists():
        for path in BENCHMARK_DOCS_DIR.glob("*.rst"):
            if path in managed_paths or not path.exists():
                continue
            text = path.read_text(encoding="utf-8")
            if GENERATED_MARKER not in text:
                continue
            changes.append(path)
            if not check:
                path.unlink()

    return changes


